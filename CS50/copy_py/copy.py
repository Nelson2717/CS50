s = input("s: ")

t = s.capitalize()

print(f"s: {s}")
print(f"t: {t}")

# This code is a simple program that takes a string input from the user and capitalizes it.
# It uses the `capitalize()` method to convert the first character of the string to uppercase
# and the rest to lowercase.
# The program then prints both the original string `s` and the capitalized string `t`.
# This is useful for formatting user input, such as names or titles.
# The program demonstrates basic string manipulation in Python.
