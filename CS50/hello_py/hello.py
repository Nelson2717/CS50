print("hello, world", end="!\n")

# This code is a simple program that prints "hello, world!" to the console.
# The `end` parameter in the `print` function specifies what to print at the end of the output.
# By default, `print` adds a newline character (`\n`) after the output.
# In this case, it is explicitly set to print a newline character after the output.
# The program demonstrates basic usage of the `print` function in Python.