names = ["Maria", "Sofia", "Lauren"]

name = input("Name: ")

if name in names:
    print("Found")
else:
    print("Not found")

# This code checks if a given name is in a predefined list of names.
# It prompts the user for a name and checks if that name is in the list.
# If the name is found, it prints "Found"; otherwise, it prints "Not found."
# The code is a simple demonstration of list membership testing in Python.